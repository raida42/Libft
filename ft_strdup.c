#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;
	size_t	size;
	size_t	i;

	size = ft_strlen(s1);
	p = (char *)malloc(sizeof(char) * (size + 1));
	if (!p)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
